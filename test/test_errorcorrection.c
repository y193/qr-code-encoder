#include "errorcorrection.h"
#include <assert.h>
#include <stdio.h>
#include <string.h>

static void test_encodeErrorCorrectionCodewords_1M(void) {
    const uint8_t dataCodewords[] = {0x10, 0x20, 0x0C, 0x56, 0x61, 0x80,
                                     0xEC, 0x11, 0xEC, 0x11, 0xEC, 0x11,
                                     0xEC, 0x11, 0xEC, 0x11};

    size_t numEcCodewords = 10;
    uint8_t ecCodewords[10];
    RSBlock rsBlock = (RSBlock){1, 16, 0, 0, 10};

    encodeErrorCorrectionCodewords(ecCodewords, dataCodewords, rsBlock);

    const uint8_t expected[] = {0xA5, 0x24, 0xD4, 0xC1, 0xED,
                                0x36, 0xC7, 0x87, 0x2C, 0x55};

    assert(memcmp(ecCodewords, expected, numEcCodewords) == 0);

    printf("test_encodeErrorCorrectionCodewords_1M() passed\n");
}

